                    <p><br> <i>I have always imagined that paradise will be a kind of library.:</i> <b>Jorge Luis Borges</b>
                                Can all the knowledge of the world be contained in one place if we could collect all the books ever written, images ever printed &amp; maps ever created, in one library ? Borges imagines just such a place in his story "<b> The Library of Babel</b>, an endless library which contains not just all the books ever made but even books yet to be made ( in the future). In another story Aleph, Borges stretches this imagination even further where man in his quest for understanding the building blocks of language itself, writes books with just one letter of the alphabet. The absurdity of it makes clear that <b>Complete knowledge of the universe is impossible for man to collect or to curate</b>:.</p>
                            <p>We aim to curate a <b>Prarang Museum</b> showcasing the Story of the Indian Civilization through the evolution of its Cities &amp; Towns, along its Rivers. Such a <b>river civilization</b> museum will be built to show-case India:s Culture &amp; Nature, on the outskirts of New Delhi, along the <b>River Ganga.</b> While this museum on the river will take a few years to build, the objects &amp; library to be housed inside the museum, have already been collected by Prarang founding team members over the past 25 years. It is this unique library of books, coins, statues, maps, comics, stamps, posters, carpets, furniture, textiles, fossils, stones &amp; other collectibles relating to Indian cities, which forms the USP ( unique selling proposition) of Prarang:s daily knowledge posts. The researchers at Shabda Chitra research center have access to this comprehensive library of books &amp; historical objects on India. This keeps a vital hand &amp; heartconnection alive for the Prarang team members. We do not re-cycle existing digital information already available on the internet, for our research work but have our own collection of copy-right free images &amp; antiquarian source books, to search from. Merely translation is not what Prarang does. We research with the intent of writing for the benefit of a specific city:s reader, fully aware of his linguistic level, education level &amp; economic need. </p>
                       
