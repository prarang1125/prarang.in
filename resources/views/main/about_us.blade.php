                                        <p class="text-center h2">Prarang Way</p>
                                        <p><br><b>The Prarang R&amp;D Process Reach. Reorganize. Re-Connect</b></p>
                                        <p>Prarang has a unique research methodology which relies on a thorough analysis of each City/Town selected, in terms of (i) the evolution of the City, (ii) what <b>Work</b>/occupations of the people of the City currently work in, (iii)the co-relation between each type of Work &amp; the <b>Natural</b> surroundings &amp; the historical evolution of the City, (iv) and the diversity of the <b>Citizens</b> living in the City. A complete review of <b>Work, Place</b> &amp; <b>Citizenship</b> results in defining a bio-regional focus for the City. Next, a dedicated Researcher &amp; Daily Content Writer is assigned to the City, and Advisors are appointed on a City Councilto advise &amp; quarterly review the City Content Calendarused by the Researcher of the City, to create Daily Knowledge &amp; Topical Postsof Global Knowledge, localized for the City &amp; shared daily through social media &amp; the android App.<br>The selection of the Knowledge posts on different streams/disciplines is a balance that is achieved keeping the bio-regional focus of the location &amp; following a 7 step methodology leading to the eventual Daily Knowledge post. The daily post is tagged &amp; classified into the City:s knowledge repository of Culture &amp; Nature, a 60 section store-house designed to support intelligent retrieval for the future needs of Smarter Citizens.</p>
                                        <p>This 7 step Process has Step-4 in the middle, as the heart of the process We actually create the content of each Knowledge Post from 3 distinct components - Data, Images &amp; Text ( Books &amp; Digital) sources. The bio-regional focus document &amp; the <br>City Council advisors then assist in defining the 4th component i.e. Intent, before the content-writer creates the daily post.</p>

